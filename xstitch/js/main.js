var canvas = {
	canvasId: 'xstitch', // The element ID to populate with our cross stitch
	font: 'font1', // Which font to use
	colCount: 1, // An internal count of column numbers

	displayLetter: function(letter, appendGap) {
		// Special characters
		if (letter == '.') {
			letter = 'fullstop';
		} else if (letter == ',') {
			letter = 'comma';
		} else if (letter == ';') {
			letter = 'semicolon';
		} else if (letter == "'" || letter == 'â€™') {
			letter = 'apostrophe';
		} else if (letter == '/') {
			letter = 'forwardSlash';
		} else if (letter == '!') {
			letter = 'exclamationMark';
		} else if (letter == '?') {
			letter = 'questionMark';
		} else if (letter == '-') {
			letter = 'hyphen';
		}

		// Default to space for unknown characters
		if (!fonts[this.font][letter]) {
			letter = 'space';
		}

		for (var x = 1; x <= fonts[this.font][letter][0][0]; x++) {
			$('<div class="col col' + this.colCount + '"></div>').appendTo($('#' + this.canvasId));
			$('<div class="row row0">' + this.colCount + '</div>').appendTo($('.col' + this.colCount + ':last'));
			for (var i = 1; i <= fonts[this.font].lineHeight; i++) {
				var output = '.';
				if (fonts[this.font][letter][i + (fonts[this.font].lineHeight * (x - 1))] == 1) {
					output = 'x';
				}
				$('<div class="row row' + i + '">' + output + '</div>').appendTo($('.col' + this.colCount + ':last'));
			}
			this.colCount++;
		}

		if (appendGap == true) {
			this.displayLetter('letterGap', false);
		}
	},

	displayLine: function(line) {
		line = line.replace(/\n/g, '');
		var lineArr = line.split('');
		for (var z = 0; z < lineArr.length; z++) {
			var appendGap = true;
			if (z == (lineArr.length - 1) || lineArr[z] == ' ') {
				appendGap = false;
			}
			this.displayLetter(lineArr[z], appendGap);
		}
	},

	insertLineBreak: function() {
		$('<div class="clearfix"></div>').appendTo($('#' + this.canvasId));
		this.colCount = 1;
	},

	displayText: function(text) {
		var textArr = text.split('\n');
		for (var z = 0; z < textArr.length; z++) {
			this.displayLine(textArr[z]);
			this.insertLineBreak();
		}
	},

	clear: function() {
		$('#' + this.canvasId).empty();
		this.colCount = 1;
	}
};

